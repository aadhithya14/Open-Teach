import math
import numpy as np

def euler2quat(euler):
    """
    Converts euler angles to quaternion format.

    Args:
        euler (np.array): (roll, pitch, yaw) vec3 float angles

    Returns:
        np.array: (x,y,z,w) vec4 quaternion
    """
    roll, pitch, yaw = euler
    cy = np.cos(yaw * 0.5)
    sy = np.sin(yaw * 0.5)
    cp = np.cos(pitch * 0.5)
    sp = np.sin(pitch * 0.5)
    cr = np.cos(roll * 0.5)
    sr = np.sin(roll * 0.5)

    return np.array(
        (
            sr * cp * cy - cr * sp * sy,
            cr * sp * cy + sr * cp * sy,
            cr * cp * sy - sr * sp * cy,
            cr * cp * cy + sr * sp * sy,
        ),
        dtype=np.float32,
    )

